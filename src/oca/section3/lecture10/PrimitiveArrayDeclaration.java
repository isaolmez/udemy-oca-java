package oca.section3.lecture10;

@SuppressWarnings("unused")
public class PrimitiveArrayDeclaration {
	public static void main(String[] args) {
		
		int[] ages; 	// brackets before name. Recommended.
		
		int ages2[]; 	// brackets after name. Legal but less readable.
		
		int [] ages3;	// spaces between the name and []. Legal, but bad.
		
		int ages4 [];	// spaces between the name and []. Legal, but bad.
	}

}
