package oca.section4.lecture4;

public class BasicArithmetic {
	public static void main(String[] args) {
		int a = 5 + 2;  // 7
		int b = 5 - 2;  // 3
		int c = 5 * 2;  // 10
		int d = 5 / 2;  // 2
		int e = 5 % 2;  // 1
		System.out.println(a + "\n" + b + "\n" +  c + "\n" + d + "\n" + e);
	}
}
